import { mkdir, readFile, rm, writeFile } from 'node:fs/promises';
import { existsSync } from 'node:fs';
import { spawn } from 'node:child_process';
import { join, resolve } from 'node:path';

const mediaDir = resolve(process.argv[2] || 'delivery/media');
const frameDir = join(mediaDir, 'walkthrough-frames');
const ffmpeg = process.env.FFMPEG_PATH || join(process.env.LOCALAPPDATA || '', 'ms-playwright', 'ffmpeg-1011', 'ffmpeg-win64.exe');
const bundledPython = join(process.env.USERPROFILE || '', '.cache', 'codex-runtimes', 'codex-primary-runtime', 'dependencies', 'python', 'python.exe');
const python = process.env.PYTHON_WITH_PIL || (existsSync(bundledPython) ? bundledPython : 'python');

const scenes = [
  ['01-home.png', '中文游戏大厅'],
  ['02-login.png', '登录'],
  ['03-create.png', '创作工坊'],
  ['04-tasks.png', '生成任务'],
  ['05-play.png', '试玩运行时']
];

if (!existsSync(ffmpeg)) throw new Error(`FFmpeg not found at ${ffmpeg}`);

await mkdir(frameDir, { recursive: true });
await rm(frameDir, { recursive: true, force: true });
await mkdir(frameDir, { recursive: true });

let index = 0;
for (const [file] of scenes) {
  const source = join(mediaDir, file);
  if (!existsSync(source)) throw new Error(`Missing screenshot ${source}`);
  const jpeg = join(frameDir, `${file.replace(/\.png$/i, '')}.jpg`);
  await renderJpeg(source, jpeg);
  const bytes = await readFile(jpeg);
  for (let repeat = 0; repeat < 18; repeat += 1) {
    await writeFile(join(frameDir, `frame-${String(index).padStart(5, '0')}.jpg`), bytes);
    index += 1;
  }
}

const videoPath = join(mediaDir, 'demo-walkthrough.webm');
const args = [
  '-y',
  '-f', 'image2pipe',
  '-vcodec', 'mjpeg',
  '-framerate', '6',
  '-i', 'pipe:0',
  '-c:v', 'libvpx',
  '-b:v', '1800k',
  '-f', 'webm',
  videoPath
];
const ok = await pipeFrames(ffmpeg, args, index);
if (!ok || !existsSync(videoPath)) throw new Error('Video encoding failed.');

await writeFile(join(mediaDir, 'README.md'), `# Demo Media

Generated by \`node scripts/capture-demo.mjs\` and \`node scripts/build-demo-video.mjs\`.

- Screenshots: ${scenes.map(([file, title]) => `${file} (${title})`).join(', ')}
- Video: demo-walkthrough.webm
- Demo account: creator@example.com / password123
- Demo URL: http://127.0.0.1:4173
- Notes: The video is assembled from browser-captured screenshots of the real local application flow.
`, 'utf8');

console.log(`video: ${videoPath}`);
console.log(`frames: ${index}`);

async function renderJpeg(source, target) {
  const code = [
    'from PIL import Image',
    'import sys',
    'img = Image.open(sys.argv[1]).convert("RGB")',
    'img.save(sys.argv[2], "JPEG", quality=88, optimize=True)'
  ].join('; ');
  const ok = await run(python, ['-c', code, source, target]);
  if (!ok || !existsSync(target)) throw new Error(`Failed to render JPEG frame ${target}`);
}

async function run(command, args) {
  return await new Promise((resolvePromise) => {
    const child = spawn(command, args, { stdio: 'ignore' });
    child.on('error', () => resolvePromise(false));
    child.on('exit', (code) => resolvePromise(code === 0));
  });
}

async function pipeFrames(command, args, frameCount) {
  return await new Promise((resolvePromise) => {
    const child = spawn(command, args, { stdio: ['pipe', 'ignore', 'ignore'] });
    child.on('error', () => resolvePromise(false));
    child.on('exit', (code) => resolvePromise(code === 0));
    child.stdin.on('error', () => {});
    (async () => {
      try {
        for (let frame = 0; frame < frameCount; frame += 1) {
          if (!child.stdin.writable) break;
          child.stdin.write(await readFile(join(frameDir, `frame-${String(frame).padStart(5, '0')}.jpg`)));
        }
        if (child.stdin.writable) child.stdin.end();
      } catch {
        child.stdin.destroy();
      }
    })();
  });
}
